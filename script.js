let currentPeriod = 'ancient';
let currentChapterIndex = -1;

// 初始化
document.addEventListener('DOMContentLoaded', () => {
    switchPeriod('ancient');
});

// 切换历史时期
function switchPeriod(period) {
    currentPeriod = period;
    
    // 更新导航按钮状态
    document.querySelectorAll('.nav-item').forEach(item => {
        item.classList.remove('active');
        if (item.getAttribute('onclick').includes(`'${period}'`)) {
            item.classList.add('active');
        }
    });

    // 更新侧边栏章节列表
    const chapterList = document.getElementById('chapter-list');
    chapterList.innerHTML = '';
    
    historyData[period].forEach((chapter, index) => {
        const div = document.createElement('div');
        div.className = 'chapter-item';
        div.textContent = chapter.title;
        div.onclick = () => showChapter(index);
        chapterList.appendChild(div);
    });

    // 重置主视图
    document.getElementById('empty-state').style.display = 'block';
    document.getElementById('main-content').style.display = 'none';
    currentChapterIndex = -1;
}

// 显示具体章节内容
function showChapter(index) {
    currentChapterIndex = index;
    const chapter = historyData[currentPeriod][index];

    // 更新侧边栏选中状态
    document.querySelectorAll('.chapter-item').forEach((item, i) => {
        if (i === index) item.classList.add('active');
        else item.classList.remove('active');
    });

    // 填充内容
    document.getElementById('empty-state').style.display = 'none';
    document.getElementById('main-content').style.display = 'block';
    
    document.getElementById('view-title').textContent = chapter.title;
    document.getElementById('view-time').textContent = `🕒 ${chapter.time}`;
    document.getElementById('view-importance').textContent = `重要程度：${chapter.importance}`;
    document.getElementById('view-image').src = chapter.image;
    document.getElementById('image-caption').textContent = chapter.imageCaption;
    document.getElementById('view-logic').textContent = chapter.logic;

    const pointsList = document.getElementById('view-points');
    pointsList.innerHTML = '';
    chapter.points.forEach(point => {
        const li = document.createElement('li');
        // 简单处理加粗
        li.innerHTML = point.replace(/【(.*?)】/g, '<strong>$1</strong>');
        pointsList.appendChild(li);
    });

    // 滚动到顶部
    document.querySelector('.content-view').scrollTop = 0;
}

// 语音朗读
function readCurrentContent() {
    if (currentChapterIndex === -1) return;
    const chapter = historyData[currentPeriod][currentChapterIndex];
    const text = `${chapter.title}。时间范围：${chapter.time}。核心考点归纳：${chapter.points.join(' ')}。逻辑串联：${chapter.logic}`;
    
    window.speechSynthesis.cancel();
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = 'zh-CN';
    utterance.rate = 0.9;
    window.speechSynthesis.speak(utterance);
}

// 章节关卡小游戏
function playChapterGame() {
    if (currentChapterIndex === -1) return;
    const chapter = historyData[currentPeriod][currentChapterIndex];
    const quiz = chapter.quiz;

    const modalBody = document.getElementById('modal-body');
    document.getElementById('modal-title').textContent = `🧠 ${chapter.title} · 知识挑战`;
    
    modalBody.innerHTML = `
        <div class="game-question">${quiz.question}</div>
        <div class="option-list">
            ${quiz.options.map(opt => `
                <div class="game-option" onclick="checkAnswer(this, '${opt}', '${quiz.answer}')">${opt}</div>
            `).join('')}
        </div>
        <div id="game-feedback" style="margin-top:20px; font-weight:bold; text-align:center;"></div>
    `;

    openModal();
}

// 检查答案
function checkAnswer(element, selected, correct) {
    const feedback = document.getElementById('game-feedback');
    const options = document.querySelectorAll('.game-option');
    
    // 禁用所有选项
    options.forEach(opt => opt.style.pointerEvents = 'none');

    if (selected === correct) {
        element.classList.add('correct');
        feedback.innerHTML = "🎉 恭喜你，答对了！";
        feedback.style.color = "var(--success)";
    } else {
        element.classList.add('wrong');
        options.forEach(opt => {
            if (opt.textContent === correct) opt.classList.add('correct');
        });
        feedback.innerHTML = `❌ 哎呀，答错了。正确答案是：${correct}`;
        feedback.style.color = "var(--danger)";
    }
}

// 终极挑战逻辑
function startMegaChallenge() {
    const allQuizzes = [];
    Object.values(historyData).forEach(period => {
        period.forEach(chapter => allQuizzes.push({
            ...chapter.quiz,
            chapterTitle: chapter.title
        }));
    });

    // 随机选10题
    const shuffled = allQuizzes.sort(() => 0.5 - Math.random()).slice(0, 10);
    let currentQuizIdx = 0;
    let score = 0;

    const renderMegaQuiz = () => {
        const q = shuffled[currentQuizIdx];
        const modalBody = document.getElementById('modal-body');
        document.getElementById('modal-title').textContent = `🏆 终极考场 (${currentQuizIdx + 1}/10)`;
        
        modalBody.innerHTML = `
            <div style="font-size:0.8rem; color:#999; margin-bottom:5px;">所属章节：${q.chapterTitle}</div>
            <div class="game-question">${q.question}</div>
            <div class="option-list">
                ${q.options.map(opt => `
                    <div class="game-option" onclick="handleMegaAnswer(this, '${opt}', '${q.answer}')">${opt}</div>
                `).join('')}
            </div>
            <div id="game-feedback" style="margin-top:20px; font-weight:bold; text-align:center;"></div>
        `;
    };

    window.handleMegaAnswer = (element, selected, correct) => {
        const options = document.querySelectorAll('.game-option');
        options.forEach(opt => opt.style.pointerEvents = 'none');

        if (selected === correct) {
            element.classList.add('correct');
            score++;
        } else {
            element.classList.add('wrong');
            options.forEach(opt => {
                if (opt.textContent === correct) opt.classList.add('correct');
            });
        }

        setTimeout(() => {
            currentQuizIdx++;
            if (currentQuizIdx < shuffled.length) {
                renderMegaQuiz();
            } else {
                showFinalScore(score, shuffled.length);
            }
        }, 1500);
    };

    renderMegaQuiz();
    openModal();
}

function showFinalScore(score, total) {
    const modalBody = document.getElementById('modal-body');
    document.getElementById('modal-title').textContent = `🎊 挑战结束`;
    
    let message = "";
    if (score === total) message = "历史大神！中考满分稳了！";
    else if (score >= total * 0.6) message = "表现不错，继续加油复习！";
    else message = "革命尚未成功，同学仍需努力。";

    modalBody.innerHTML = `
        <div style="text-align:center; padding:20px;">
            <div style="font-size:3rem;">${score === total ? '🎖️' : '📝'}</div>
            <h2 style="margin:15px 0;">得分：${score} / ${total}</h2>
            <p style="color:#666;">${message}</p>
            <button class="btn btn-game" style="margin-top:20px;" onclick="closeModal()">返回复习</button>
        </div>
    `;
}

